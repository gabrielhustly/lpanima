interface PixelIconProps {
  name: IconName;
  size?: number;
  className?: string;
  accentClassName?: string;
}

export function PixelIcon({
  name,
  size = 32,
  className,
  accentClassName,
}: PixelIconProps) {
  const grid = ICONS[name];
  const rows = grid.length;
  const cols = grid[0].length;

  return (
    <svg
      width={size}
      height={size}
      viewBox={`0 0 ${cols} ${rows}`}
      shapeRendering="crispEdges"
      className={className}
      aria-hidden="true"
    >
      {grid.map((row, y) =>
        row.map((v, x) => {
          if (v === 0) return null;
          return (
            <rect
              key={`${x}-${y}`}
              x={x}
              y={y}
              width={1}
              height={1}
              className={v === 2 ? accentClassName ?? "fill-hustly-lime" : "fill-current"}
            />
          );
        })
      )}
    </svg>
  );
}

export type { IconName };